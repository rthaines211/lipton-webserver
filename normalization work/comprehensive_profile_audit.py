    print("="*80)
    print("PODS PROFILE AUDIT")
    print("="*80)

    # Find count mismatches
    print("\nüìä COUNT DISCREPANCIES:")
    mismatches = []

    for flag in sorted(PODS_SPEC.keys()):
        spec_count = PODS_SPEC[flag]
        code_count = PODS_CODE.get(flag, "MISSING")

        if code_count == "MISSING":
            mismatches.append((flag, spec_count, "MISSING", f"-{spec_count}"))
        elif spec_count != code_count:
            diff = code_count - spec_count
            mismatches.append((flag, spec_count, code_count, f"{diff:+d}"))

    if mismatches:
        print(f"\nFound {len(mismatches)} discrepancies:\n")
        print(f"{'Flag':<50} {'Spec':<10} {'Code':<10} {'Diff':<10}")
        print("-"*80)
        for flag, spec, code, diff in mismatches:
            symbol = "‚ùå" if (isinstance(code, int) and abs(code - spec) >= 3) or code == "MISSING" else "‚ö†Ô∏è"
            print(f"{symbol} {flag:<47} {spec:<10} {str(code):<10} {diff:<10}")

    print(f"\n{'='*80}\n")

