check_env() {
    local NAME=$1
    local URL=$2
    
    echo "‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ"
    echo "  $NAME ENVIRONMENT"
    echo "‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ"
    
    HEALTH=$(curl -s "$URL/health" 2>&1)
    
    if echo "$HEALTH" | grep -q "healthy"; then
        echo "‚úÖ Service: HEALTHY"
        
        BRANCH=$(echo "$HEALTH" | jq -r '.deploymentInfo.branch' 2>/dev/null)
        UPTIME=$(echo "$HEALTH" | jq -r '.uptime' 2>/dev/null)
        
        echo "üìç Branch: $BRANCH"
        echo "‚è±Ô∏è  Uptime: $(printf "%.1f" $UPTIME)s"
        
        if [ "$BRANCH" = "main" ]; then
            echo "üéâ REFACTORING DEPLOYED!"
        elif [ "$BRANCH" = "dev/intake-system" ]; then
            if [ "$UPTIME" != "null" ] && [ $(echo "$UPTIME < 120" | bc -l) -eq 1 ]; then
                echo "‚è≥ Deployment in progress (recently restarted)"
            else
                echo "‚ö†Ô∏è  Old deployment still running"
            fi
        fi
    else
        echo "‚ùå Service: UNHEALTHY or UNREACHABLE"
        echo "$HEALTH" | head -5
    fi
    echo ""
}

